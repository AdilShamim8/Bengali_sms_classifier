def generate_message():
    nums_of_sent = random.randint(3,5)
    parts = []

    for _ in range(nums_of_sent):
        category = random.choice([
            loan, lottery, jobs, dating, health_spam, security_alerts,
            contests, investment, adult, miracle_health, subscription
        ])
        sentence = random.choice(category)
        parts.append(sentence)  
    return " ".join(parts)

unique_msg = set()
while len(unique_msg) <2000:
    unique_msg.add(generate_message())


df = pd.DataFrame({
    'label': ['spam']*len(unique_msg),
    'text': list(unique_msg)
})
df.to_csv('sms_dataset.csv', index=False, encoding='utf-8')
