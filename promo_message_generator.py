def generate_message():
    nums_of_sent = random.randint(3,5)
    parts = []

    for _ in range(nums_of_sent):
        category = random.choice([
            books_education, fashion_accessories, electronics, food_restaurants,
            home_appliances, general_store_promotions, fitness_health_products,
            entertainment, public_awareness
        ])
        sentence = random.choice(category)
        parts.append(sentence)  
    return " ".join(parts)

unique_msg = set()
while len(unique_msg) <2000:
    unique_msg.add(generate_message())


df = pd.DataFrame({
    'label': ['promo']*len(unique_msg),
    'text': list(unique_msg)
})
df.to_csv('promo_sms_dataset.csv', index=False, encoding='utf-8')
