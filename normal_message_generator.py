def generate_messages():
    num_sentences = random.randint(3,5)
    parts = []
    for _ in range(num_sentences):
        category = random.choice([greetings,daily,friendship,closing,family,food,shopping,travel,studies,health,entertainment])
        parts.append(random.choice(category))
    return " ".join(parts)

unique_msg = set()
while(len(unique_msg) < 2000):
    unique_msg.add(generate_messages())

df = pd.DataFrame({
    'label': ['normal'] * len(unique_msg),
    'text': list(unique_msg)
})

# Save as CSV
df.to_csv('bengali_normal_sms_dataset.csv', index=False, encoding='utf-8')
